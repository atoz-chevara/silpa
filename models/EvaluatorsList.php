    // Get Row Hash
    public function getRowHash(&$rs)
    {
        if (!$rs) {
            return "";
        }
        $row = ($rs instanceof Recordset) ? $rs->fields : $rs;
        $hash = "";
        $hash .= GetFieldHash($row['nip']); // nip
        $hash .= GetFieldHash($row['nama_lengkap']); // nama_lengkap
        $hash .= GetFieldHash($row['wilayah']); // wilayah
        $hash .= GetFieldHash($row['idd_user']); // idd_user
        return md5($hash);
    }

